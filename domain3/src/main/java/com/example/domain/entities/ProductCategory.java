package com.example.domain.entities;

public class ProductCategory {


    private String id;

    private String name;

    private String description;

    private String image;

    public ProductCategory(String id, String name, String description, String image) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.image = image;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getImage() {
        return image;
    }
}
